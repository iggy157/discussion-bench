"""Select HiddenBench eval tasks + script-source tasks from a baseline difficulty profiling.

baselineプロファイリング（results/hb_profiling/task*.json, 各タスク1ゲーム）の pre/post 正答率から、
壊れ/簡単すぎを除外し、良問プールを種別で層化 → 台本ソース K件 と 評価セット を決める。

分類（1ゲーム=粗いので明確なケースのみ機械的に弾く）:
  - broken : post == 0            （議論しても解けない／壊れ）→ 除外
  - trivial: pre  >= TRIVIAL_PRE  （一人で解ける＝hidden-profile性が弱い）→ 除外
  - valid  : それ以外（pre低 × post>0）→ 評価候補
台本ソースは valid から種別層化＋固定seedで K件、評価はそれ以外の valid 全部。
出力: 標準出力にサマリ、--out に YAML（eval_ids / script_ids）。

Usage:
    python eval/src/select_hb_tasks.py --profiling results/hb_profiling \
        --benchmark server/hidden-bench/data/benchmark.json --k 5 --seed 42 --out config/hb_task_split.yml
"""

from __future__ import annotations

import argparse
import collections
import glob
import json
import random
import re
from pathlib import Path

TRIVIAL_PRE = 0.5  # pre >= this -> solvable alone -> weak hidden-profile -> exclude from eval


def categorize(name: str) -> str:
    """Rough scenario typology from the task name (for stratified script-source picking)."""
    n = name.lower()
    if re.search(r"(18|19|20)\d{2}", n) or "_et_al" in n or "stasser" in n or "schulz" in n:
        return "literature"
    if any(w in n for w in ("theft", "missing", "mystery", "outbreak", "prototype",
                            "sighting", "deduction", "sample", "medicine", "elusive")):
        return "deduction"
    if any(w in n for w in ("investor", "acquisition", "lunch", "banquet", "meeting", "negotiation")):
        return "decision"
    return "scenario"  # the large evacuation/shelter/site-selection family


def main() -> None:
    p = argparse.ArgumentParser()
    p.add_argument("--profiling", default="results/hb_profiling")
    p.add_argument("--benchmark", default="server/hidden-bench/data/benchmark.json")
    p.add_argument("--k", type=int, default=5, help="number of script-source tasks to hold out")
    p.add_argument("--seed", type=int, default=42)
    p.add_argument("--trivial-pre", type=float, default=TRIVIAL_PRE)
    p.add_argument("--out", default="config/hb_task_split.yml")
    args = p.parse_args()

    names = {t["id"]: t.get("name", str(t["id"])) for t in json.loads(Path(args.benchmark).read_text())}

    rows = {}
    for f in glob.glob(str(Path(args.profiling) / "task*.json")):
        d = json.loads(Path(f).read_text())
        tid = d.get("task_id")
        if tid is None:
            m = re.search(r"hb-(\d+)-", d.get("game_id", ""))
            tid = int(m.group(1)) if m else None
        if tid is None:
            continue
        rows[tid] = {"pre": d.get("pre_accuracy"), "post": d.get("post_accuracy"),
                     "name": names.get(tid, str(tid)), "cat": categorize(names.get(tid, ""))}

    broken, trivial, valid = [], [], []
    for tid, r in rows.items():
        if r["post"] == 0:
            broken.append(tid)
        elif r["pre"] is not None and r["pre"] >= args.trivial_pre:
            trivial.append(tid)
        else:
            valid.append(tid)

    # stratified script-source pick from valid pool
    rnd = random.Random(args.seed)
    bycat: dict[str, list[int]] = collections.defaultdict(list)
    for tid in valid:
        bycat[rows[tid]["cat"]].append(tid)
    for c in bycat:
        rnd.shuffle(bycat[c])
    script_ids: list[int] = []
    cats = sorted(bycat, key=lambda c: -len(bycat[c]))  # round-robin, larger cats first
    i = 0
    while len(script_ids) < min(args.k, len(valid)):
        c = cats[i % len(cats)]
        if bycat[c]:
            script_ids.append(bycat[c].pop())
        i += 1
        if i > 1000:
            break
    script_ids.sort()
    eval_ids = sorted([t for t in valid if t not in script_ids])

    print(f"profiled tasks: {len(rows)}  | broken(post=0): {len(broken)} {sorted(broken)}")
    print(f"trivial(pre>={args.trivial_pre}): {len(trivial)} {sorted(trivial)}")
    print(f"valid pool: {len(valid)}  by category: { {c: len(v) for c, v in bycat.items()} }")
    print(f"\n=> SCRIPT-SOURCE (held-out, K={len(script_ids)}, stratified seed={args.seed}):")
    for t in script_ids:
        print(f"     {t:>3} [{rows[t]['cat']:10s}] pre={rows[t]['pre']} post={rows[t]['post']} {rows[t]['name']}")
    print(f"=> EVAL set: {len(eval_ids)} tasks -> {eval_ids}")

    out = Path(args.out)
    out.parent.mkdir(parents=True, exist_ok=True)
    lines = ["# Auto-generated by select_hb_tasks.py — HiddenBench eval/script-source split.",
             f"# valid={len(valid)} broken={len(broken)} trivial={len(trivial)} (seed={args.seed}, k={args.k})",
             "eval_ids: [" + ", ".join(map(str, eval_ids)) + "]",
             "script_ids: [" + ", ".join(map(str, script_ids)) + "]",
             "broken_ids: [" + ", ".join(map(str, sorted(broken))) + "]",
             "trivial_ids: [" + ", ".join(map(str, sorted(trivial))) + "]"]
    out.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"\nwrote {out}")


if __name__ == "__main__":
    main()
